import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');
const contractPath = path.join(repoRoot, 'contracts', 'sync-contract.json');

const contract = JSON.parse(await readFile(contractPath, 'utf8'));

const targets = [
  {
    path: path.join(repoRoot, 'src', 'generated', 'syncContract.ts'),
    contents: buildTypeScript(contract),
  },
  {
    path: path.join(repoRoot, 'backend', 'src', 'main', 'java', 'com', 'meditation', 'backend', 'sync', 'GeneratedSyncContract.java'),
    contents: buildJava(contract),
  },
  {
    path: path.join(repoRoot, 'ios-native', 'Sources', 'MeditationNativeCore', 'Domain', 'GeneratedSyncContract.swift'),
    contents: buildSwift(contract),
  },
];

for (const target of targets) {
  await mkdir(path.dirname(target.path), { recursive: true });
  await writeFile(target.path, target.contents, 'utf8');
}

function buildTypeScript(value) {
  return [
    '// Generated by scripts/generate-sync-contract.mjs. Do not edit manually.',
    `export const generatedSyncContract = ${JSON.stringify(value, null, 2)} as const;`,
    '',
    'export const syncQueuedAtHeader = generatedSyncContract.sync.headers.queuedAt;',
    'export const syncResultHeader = generatedSyncContract.sync.headers.result;',
    'export const syncOutcomeApplied = generatedSyncContract.sync.outcomes.applied;',
    'export const syncOutcomeStale = generatedSyncContract.sync.outcomes.stale;',
    'export const syncOutcomeDeleted = generatedSyncContract.sync.outcomes.deleted;',
    'export const syncDeleteCurrentRecordField = generatedSyncContract.sync.delete.currentRecordField;',
    'export const syncDeleteLegacyAliasFields = generatedSyncContract.sync.delete.legacyAliasFields;',
    'export const meditationTypes = generatedSyncContract.referenceData.meditationTypes;',
    'export const sessionLogSources = generatedSyncContract.referenceData.sessionLogSources;',
    'export const timeOfDayBuckets = generatedSyncContract.referenceData.timeOfDayBuckets;',
    'export const goalTypes = generatedSyncContract.referenceData.goalTypes;',
    'export const observanceStatuses = generatedSyncContract.referenceData.observanceStatuses;',
    'export const timerModes = generatedSyncContract.referenceData.timerModes;',
    'export const sessionLogStatuses = generatedSyncContract.referenceData.sessionLogStatuses;',
    '',
  ].join('\n');
}

function buildJava(value) {
  const {
    sync,
    referenceData,
  } = value;

  return `package com.meditation.backend.sync;

import java.util.List;

// Generated by scripts/generate-sync-contract.mjs. Do not edit manually.
public final class GeneratedSyncContract {

  public static final int SCHEMA_VERSION = ${value.schemaVersion};
  public static final String SYNC_QUEUED_AT_HEADER = "${sync.headers.queuedAt}";
  public static final String SYNC_RESULT_HEADER = "${sync.headers.result}";
  public static final String SYNC_OUTCOME_APPLIED = "${sync.outcomes.applied}";
  public static final String SYNC_OUTCOME_STALE = "${sync.outcomes.stale}";
  public static final String SYNC_OUTCOME_DELETED = "${sync.outcomes.deleted}";
  public static final String DELETE_CURRENT_RECORD_FIELD = "${sync.delete.currentRecordField}";
  public static final String DELETE_CUSTOM_PLAY_ALIAS_FIELD = "${sync.delete.legacyAliasFields.customPlay}";
  public static final String DELETE_PLAYLIST_ALIAS_FIELD = "${sync.delete.legacyAliasFields.playlist}";
  public static final String DELETE_SANKALPA_ALIAS_FIELD = "${sync.delete.legacyAliasFields.sankalpa}";
  public static final List<String> MEDITATION_TYPES = ${toJavaList(referenceData.meditationTypes)};
  public static final List<String> SESSION_LOG_SOURCES = ${toJavaList(referenceData.sessionLogSources)};
  public static final List<String> TIME_OF_DAY_BUCKETS = ${toJavaList(referenceData.timeOfDayBuckets)};
  public static final List<String> GOAL_TYPES = ${toJavaList(referenceData.goalTypes)};
  public static final List<String> OBSERVANCE_STATUSES = ${toJavaList(referenceData.observanceStatuses)};
  public static final List<String> TIMER_MODES = ${toJavaList(referenceData.timerModes)};
  public static final List<String> SESSION_LOG_STATUSES = ${toJavaList(referenceData.sessionLogStatuses)};

  private GeneratedSyncContract() {
  }
}
`;
}

function buildSwift(value) {
  const {
    sync,
    referenceData,
  } = value;

  return `import Foundation

// Generated by scripts/generate-sync-contract.mjs. Do not edit manually.
public enum GeneratedSyncContract {
    public static let schemaVersion = ${value.schemaVersion}
    public static let syncQueuedAtHeader = "${sync.headers.queuedAt}"
    public static let syncResultHeader = "${sync.headers.result}"
    public static let syncOutcomeApplied = "${sync.outcomes.applied}"
    public static let syncOutcomeStale = "${sync.outcomes.stale}"
    public static let syncOutcomeDeleted = "${sync.outcomes.deleted}"
    public static let deleteCurrentRecordField = "${sync.delete.currentRecordField}"
    public static let deleteLegacyAliasFields: [String: String] = [
        "customPlay": "${sync.delete.legacyAliasFields.customPlay}",
        "playlist": "${sync.delete.legacyAliasFields.playlist}",
        "sankalpa": "${sync.delete.legacyAliasFields.sankalpa}",
    ]
    public static let meditationTypes = ${toSwiftArray(referenceData.meditationTypes)}
    public static let sessionLogSources = ${toSwiftArray(referenceData.sessionLogSources)}
    public static let timeOfDayBuckets = ${toSwiftArray(referenceData.timeOfDayBuckets)}
    public static let goalTypes = ${toSwiftArray(referenceData.goalTypes)}
    public static let observanceStatuses = ${toSwiftArray(referenceData.observanceStatuses)}
    public static let timerModes = ${toSwiftArray(referenceData.timerModes)}
    public static let sessionLogStatuses = ${toSwiftArray(referenceData.sessionLogStatuses)}
}
`;
}

function toJavaList(values) {
  return `List.of(${values.map((value) => `"${value}"`).join(', ')})`;
}

function toSwiftArray(values) {
  return `[${values.map((value) => `"${value}"`).join(', ')}]`;
}
